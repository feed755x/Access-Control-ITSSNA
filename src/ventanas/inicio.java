    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
            String ncontrolcheck = numero.getText();

            if(ncontrolcheck.length()==0 || ncontrolcheck.length()>8){
                JOptionPane.showMessageDialog(null, "Numero de control vacio o no valido");
            
            }else{
            String nombre,apellidop,apellidom,carrera,semestre;
            modificar modificar1 = new modificar();
            
            String sql = ("SELECT nombre, apellidop, apellidom, carrera, semestre FROM alumnos WHERE ncontrol="+"'"+ncontrolcheck+"'"); 
          
            try{
            pst = con.prepareStatement(sql);
            rst = pst.executeQuery();
            
            nombre =  rst.getString("nombre");
            apellidop =  rst.getString("apellidop");
            apellidom = rst.getString("apellidom");
            carrera = rst.getString("carrera");
            semestre = rst.getString("semestre");
            
            modificar1.nombrea.setText(nombre);
            modificar1.apellidopaterno.setText(apellidop);
            modificar1.apellidomaterno.setText(apellidom);
            modificar1.numerocontrol.setText(ncontrolcheck);
            
             }catch(Exception e){
              JOptionPane.showMessageDialog(null, "Error al procesar los datos");                      // JOptionPane.showMessageDialog(null,e);
             }finally{
            try{
            rst.close();
                pst.close();
            }catch(Exception e){
                
            }
            }
            
        
            this.dispose();
            
            modificar1.setVisible(true);
            this.setVisible(false);
            }
    }//GEN-LAST:event_modificarActionPerformed

