    private void generarcodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarcodigoActionPerformed
        
        Image img;
        
        String numeroconsulta = numero.getText();
        String nombrealumno = name.getText();
        
        if(numeroconsulta.length()==0 || nombrealumno.length()==0){
            JOptionPane.showMessageDialog(null, "Ingresar numero de control valido y realizar consulta");
        }else{

        try {

            Document doc = new Document();
            PdfWriter pdf = PdfWriter.getInstance(doc,new FileOutputStream("Codigo alumno "+numeroconsulta+".pdf"));
            doc.open();
            Barcode39 code = new Barcode39();

            
            code.setCode(numeroconsulta);
            img = code.createImageWithBarcode(pdf.getDirectContent(), BaseColor.BLACK, BaseColor.BLACK);
            img.scalePercent(150);
            doc.add(img);
            doc.add(new Paragraph(""));
            
            doc.close();
            JOptionPane.showMessageDialog(null, "Codigo de barras generado correctamente");
        
            } catch (DocumentException | FileNotFoundException ex) {
            Logger.getLogger(inicio.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Error al generar el codigo");
            }
        
                    
        }
        
        name.setText("");
        apellip.setText("");
        apellim.setText("");
        numero.setText("");
        carr.setText("");
        sem.setText("");
    }//GEN-LAST:event_generarcodigoActionPerformed

    private void generartodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generartodosActionPerformed
        Image img;
        
        String numeroconsulta = numero.getText();
        
        try {
         String sql = ("SELECT ncontrol FROM alumnos");
         pst = con.prepareStatement(sql);
         rst = pst.executeQuery();
         
            Document doc = new Document();
            PdfWriter pdf = PdfWriter.getInstance(doc,new FileOutputStream("Codigos alumnos.pdf"));
            doc.open();
            Barcode39 code = new Barcode39();
            
            while(rst.next()){
            code.setCode(rst.getString("ncontrol"));
            img = code.createImageWithBarcode(pdf.getDirectContent(), BaseColor.BLACK, BaseColor.BLACK);
            img.scalePercent(150);
            doc.add(img);
            doc.add(new Paragraph(""));
            }
            
            doc.close();
            JOptionPane.showMessageDialog(null, "Codigos de barras generados correctamente");
        
            } catch (DocumentException ex) {
            Logger.getLogger(inicio.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Error al generar el codigo");
            } catch (FileNotFoundException ex) {
            Logger.getLogger(inicio.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Error al generar el codigo");
            } catch (SQLException ex) {
            Logger.getLogger(inicio.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Error al generar el codigo");
            }
    }//GEN-LAST:event_generartodosActionPerformed

